package ga.susite.scfw2d;

/**
 * Stores information about the scene.
 */
public class SceneInfo {
	/**
	 * The name of the scene.
	 */
	public String name;
	
	/**
	 * Initialize the scene information object with the specified name.
	 * @param name The name of the scene.
	 */
	public SceneInfo(String name) {
		this.name = name;
	}
}
