package ga.susite.scfw2d;

/**
 * @author lines-of-codes
 * This enum is an option for the Option dialog.
 */
public enum OptionDialogOption {
	/**
	 * Totally disable the option dialog.
	 */
	disabled,
	/**
	 * Enable option dialog if specified in the command line arguments.
	 */
	engineArgs,
	/**
	 * Always enable the Option dialog.
	 */
	enabled
}
